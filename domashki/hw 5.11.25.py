# 1.Пользователь вводит число n. Выведите n первых элементов
# последовательности 1, -0.5, 0.25, ... (деление на -2)
n=int(input())
a=1
for _ in range(n):
    a=a/(-2)
    print(a)
# 2.Пользователь вводит строку и символ. Посчитайте, сколько
# раз этот символ встречается (без .count())
a=input()
b=input()
count=0
for i in a:
    if i in b:
        count+=1
print(count)
